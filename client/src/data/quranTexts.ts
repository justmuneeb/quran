      "قَالُوا يَا وَيْلَنَا مَن بَعَثَنَا مِن مَّرْقَدِنَا ۜ هَٰذَا مَا وَعَدَ الرَّحْمَٰنُ وَصَدَقَ الْمُرْسَلُونَ",
      "إِن كَانَتْ إِلاَّ صَيْحَةً وَاحِدَةً فَإِذَا هُمْ جَمِيعٌ لَّدَيْنَا مُحْضَرُونَ",
      "فَالْيَوْمَ لا تُظْلَمُ نَفْسٌ شَيْئاً وَلا تُجْزَوْنَ إِلاَّ مَا كُنتُمْ تَعْمَلُونَ",
