      "لَهُمْ فِيهَا فَاكِهَةٌ وَلَهُم مَّا يَدَّعُونَ",
      "سَلامٌ قَوْلاً مِن رَّبٍّ رَّحِيمٍ",
      "وَامْتَازُوا الْيَوْمَ أَيُّهَا الْمُجْرِمُونَ",
